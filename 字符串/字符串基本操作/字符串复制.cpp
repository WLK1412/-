#include <bits/stdc++.h>
using namespace std;
int main()
{
  ios::sync_with_stdio(0);
  cin.tie(0);
  cout.tie(0);
  string source = "Hello, World!";
  string destination = source; // 直接使用赋值操作符复制字符串

  cout << "源字符串: " << source << endl;
  cout << "复制后的字符串: " << destination << endl;

  return 0;
}
